#import "Location.h"

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
@implementation Location

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
- (instancetype)initWithLongitude:(NSNumber *)longitude
                         latitude:(NSNumber *)latitude
{
    if (self = [super init]) {
        self.longitude = longitude;
        self.latitude  = latitude;
    }
    
    return self;
}

@end
